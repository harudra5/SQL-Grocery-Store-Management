-- Which products have the highest total sales volume (by quantity)?
SELECT prod_id, SUM(quantity) AS total_sales
FROM order_details
GROUP BY prod_id
ORDER BY total_sales DESC;

-- What is the total revenue generated by each product? 
SELECT prod_id, SUM(total_price) AS total_revenue
FROM order_details
GROUP BY prod_id;

-- How do product sales vary by category and supplier? 
SELECT p.supp_id, p.cat_id, SUM(od.total_price) AS sales
FROM products p JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.cat_id,p.supp_id
ORDER BY sales DESC;

-- 3. Sales and Order Trends 
-- How many orders have been placed in total?
SELECT COUNT(ord_id) AS total
FROM orders;

-- What is the average value per order? 
SELECT AVG(order_total) AS avg_value
FROM (SELECT ord_id, SUM(total_price) AS order_total
FROM order_details
GROUP BY ord_id) t;

-- On which dates were the most orders placed?
SELECT order_date,
       COUNT(*) AS total_orders
FROM orders
GROUP BY order_date
HAVING COUNT(*) = (
    SELECT MAX(order_count)
    FROM (
        SELECT COUNT(*) AS order_count
        FROM orders
        GROUP BY order_date
    ) t
);

-- What are the monthly trends in order volume and revenue? 
SELECT MONTH(str_to_date(o.order_date, '%m/%d/%Y')) AS month, SUM(od.quantity) AS valume, SUM(od.total_price) AS revenue
FROM orders as o JOIN order_details AS od ON o.ord_id = od.ord_id
GROUP BY month
ORDER BY month DESC;

-- How do order patterns vary across weekdays and weekends? 
SELECT 
	CASE 
		WHEN dayofweek(str_to_date(order_date, '%m/%d/%Y')) IN (1, 7) THEN 'Weekend'
        ELSE 'Weekday'
	END AS day_type,
    COUNT(*) AS total_orders
FROM orders
GROUP BY day_type;

