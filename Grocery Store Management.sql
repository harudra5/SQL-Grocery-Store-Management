-- 1 . Customer Insights 
-- How many unique customers have placed orders? 
SELECT COUNT(DISTINCT cust_id) AS total_orders FROM orders;

-- Which customers have placed the highest number of orders? 
SELECT cust_id, COUNT(ord_id) AS total_orders FROM orders GROUP BY cust_id ORDER BY total_orders DESC;

-- What is the total and average purchase value per customer? 
SELECT cust_id, SUM(total_price) AS total_purchase, AVG(total_price) AS avg_purchase
FROM orders o JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY cust_id;

-- Who are the top 5 customers by total purchase amount?
SELECT cust_id, SUM(total_price) AS total_purchase
FROM orders o JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY cust_id
ORDER BY total_purchase DESC
LIMIT 5;

